const day = 'thursday';

switch(day) {
  case 'monday':
    console.log('Its monday today');
    break;
  case 'tuesday':
    console.log('Its tuesday');
    break;
  case 'wednesday':
    console.log('Its wednesday');
    break;
  case 'tursday':
    console.log('Its tursday');
    break;
  case 'friday':
    console.log('Its friday');
    break;
  case 'saturday':
    console.log('Its saturday');
    break;
  case 'sunday':
    console.log('Its sunday');
    break;
  default:
    console.log('Not a valid day');
}